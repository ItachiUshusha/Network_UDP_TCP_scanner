import socket

def scan_ports(host, start_port, end_port):
    # –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –≤—Å–µ–º –ø–æ—Ä—Ç–∞–º –≤ –∑–∞–¥–∞–Ω–Ω–æ–º –¥–∏–∞–ø–∞–∑–æ–Ω–µ
    for port in range(start_port, end_port + 1):
        # –°–æ–∑–¥–∞–µ–º —Å–æ–∫–µ—Ç –¥–ª—è TCP —Å–æ–µ–¥–∏–Ω–µ–Ω–∏—è
        sock = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        sock.settimeout(0.3)  # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Ç–∞–π–º–∞—É—Ç –≤ 0.5 —Å–µ–∫—É–Ω–¥

        # –ü—ã—Ç–∞–µ–º—Å—è —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å TCP —Å–æ–µ–¥–∏–Ω–µ–Ω–∏–µ —Å —Ü–µ–ª–µ–≤—ã–º —Ö–æ—Å—Ç–æ–º –∏ –ø–æ—Ä—Ç–æ–º
        result = sock.connect_ex((host, port))

        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –æ—Ç–∫—Ä—ã—Ç –ª–∏ –ø–æ—Ä—Ç
        if result == 0:
            print(f"–ü–æ—Ä—Ç {port} –æ—Ç–∫—Ä—ã—Ç")
        else:
            print(f"–ü–æ—Ä—Ç {port} –∑–∞–∫—Ä—ã—Ç")

        # –ó–∞–∫—Ä—ã–≤–∞–µ–º —Å–æ–∫–µ—Ç üëå
        sock.close()


host = ""  # –¶–µ–ª–µ–≤–æ–π —Ö–æ—Å—Ç
start_port = int(input("–í–≤–µ–¥–∏—Ç–µ —Å –∫–∞–∫–æ–≥–æ –ø–æ—Ä—Ç–∞ –Ω–∞—á–∞—Ç—å —Å–∫–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ: "))  # –°—Ç–∞—Ä—Ç–æ–≤—ã–π –ø–æ—Ä—Ç
end_port = int(input("–í–≤–µ–¥–∏—Ç–µ –¥–æ –∫–∞–∫–æ–≥–æ –ø–æ—Ä—Ç–∞ —Å–∫–∞–Ω–∏—Ä–æ–≤–∞—Ç—å: "))  # –ö–æ–Ω–µ—á–Ω—ã–π –ø–æ—Ä—Ç

scan_ports(host, start_port, end_port)
